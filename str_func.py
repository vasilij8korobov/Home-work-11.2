def upper_chair(text):
    """ДЕЛАЕТ ВСЕ БУКВЫ БОЛЬШИМИ"""
    return text.upper()

def capitalize_first_chair(text):
    """Эта Функция Делает Заглавной Первую Букву Каждого Слова Текста"""
    return text.capitalize()

def lower_chair(text):
    """делает все буквы мелкими"""
    return text.lower()
